//写一个代码：打印100~200之间的素数
//!!素数（质数）：即只能被1和它本身整除的数（2也是素数）
//! 判断i是否为素数：从2~i-1是否有能被i整除的数，有则不是素数
#include<stdio.h>
int main()
{
	int i;
	for (i = 100; i <= 200; i++)
	{
		int n = 1;//!!这个的位置要放对！！
		int j = 0;
		for (j = 2; j < i; j++)
		{
			if (i % j == 0)
			{
				n = 0;
				break;
			}
		}
		if (n)
			printf("%d ", i);
	}
}